    /// Music.app stores FourCC as little-endian bytes of the BE code (`M4A ` → ` A4M`).
    static func writeFiletypeMarker(_ data: inout Data, at offset: Int, fourCC: String) {
        var padded = Array(fourCC.utf8.prefix(4))
        while padded.count < 4 { padded.append(0x20) }
        for i in 0..<4 {
            data[offset + i] = padded[3 - i]
        }
    }

    static func filetypeFourCC(for labelOrExt: String) -> String {
        let ext = labelOrExt.lowercased()
        if ext.contains("mpeg") || ext.contains("mp3") { return "MP3 " }
        if ext.contains("aac") || ext.contains("m4a") || ext.contains("m4p") || ext.contains("alac") { return "M4A " }
        if ext.contains("wav") { return "WAVE" }
        if ext.contains("aiff") || ext.contains("aif") { return "AIFF" }
        return "M4A "
    }

    static func codecFlags(for fourCC: String) -> (UInt8, UInt8) {
        switch fourCC {
        case "MP3 ": return (0, 1)
        default: return (0, 0)
        }
    }

    static func randomMusicFileName(extension ext: String) -> String {
        let alphabet = Array("ABCDEFGHIJKLMNOPQRSTUVWXYZ")
        let name = String((0..<4).map { _ in alphabet.randomElement()! })
        return "\(name).\(ext)"
    }
